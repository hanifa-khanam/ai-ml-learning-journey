""" USED TO ASSIGN
                 VALUES TO VARIABLES """

# (=) : assign 3 to x
a = 3
print(a)

# (+=)
a += 3      # a = a + 3
print(a)    # a = 6

# (-=)
a -= 4     # a = a - 4 => 6 - 4
print(a)   # a = 2

# (*=)
a *= 5     # a = a * 5 => 2 * 5
print(a)   # a = 10

# (/=)
a /= 4     # a = a / 4 => 10 / 4
print(a)   # a = 2.5

# (//=)
a //= 4     # a = a // 4 => 2.5 // 4
print(a)    # a = 0.0

# (%=)
a %= 3       # a = a % 3 => 0.0 % 3
print(a)     # 0.0

# (**=)   
a **= 3      # a = a ** 3 => 0.0 ** 3
print(a)     # a = 0.0
